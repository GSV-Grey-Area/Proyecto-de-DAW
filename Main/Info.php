    <h1>Sobre la página</h1>

    <p>En nuestra página, te ofrecemos una experiencia única de comparación de productos. A diferencia
    de otras, aquí puedes filtrar los resultados con precisión, lo que te permite tomar decisiones más
    informadas, precisas, y rápidas, por eliminar ruido. La presentación de los resultados en un gráfico
    facilita la diferenciación entre ellos, permitiendo identificar rápidamente cuál se ajusta mejor a lo
    que se pide. En resumen, nuestra plataforma está diseñada para brindarte la información que
    necesitas de manera clara y concisa, para que puedas tomar decisiones de compra más inteligentes y
    satisfactorias.
    Aunque hay muchas páginas dedicadas a comparar cosas o productos, al final hay que usar varias a
    la vez para compensar sus carencias. Una herramienta como esta, que podría llenar ese vacío y
    proporcionar una forma conveniente y eficiente de comparar cosas o productos es relevante en un
    mercado saturado de opciones. Además, su enfoque en la simplicidad y la usabilidad hace que sea
    accesible para una amplia gama de usuarios, lo que aumenta su valor aún más.
    </p>

    <h1>Uso</h1>

    <p>En la página principal deberá seleccionar una de las categorías
    mostradas en la pantalla. Una vez seleccionada una de las categorías le llevará a otra página
    donde se mostrara el grafico con los productos de la categoria seleccionada.
    En esa página se eligen filtros y las dos características principales según las cuales distribuir
    los resultados en el gráfico. El gráfico se actualizará con los
    nuevos parámetros, debajo del gráfico apareceran los productos enumerados los cuales corresponden
    con los números en el gráfico.
    </p>

